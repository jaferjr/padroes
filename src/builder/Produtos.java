package builder;

public class Produtos {

	String batata;
	String sanduba;
	String brinquedo;
	String refri;

	public String getBatata() {
		return batata;
	}

	public void setBatata(String batata) {
		this.batata = batata;
	}

	public String getSanduba() {
		return sanduba;
	}

	public void setSanduba(String sanduba) {
		this.sanduba = sanduba;
	}

	public String getBrinquedo() {
		return brinquedo;
	}

	public void setBrinquedo(String brinquedo) {
		this.brinquedo = brinquedo;
	}

	public String getRefri() {
		return refri;
	}

	public void setRefri(String refri) {
		this.refri = refri;
	}

}
